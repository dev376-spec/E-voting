        <p class="style1"><b>Q.9  Is time period for e-Voting same as Postal Ballot?</b></p> <br />
        <b> Ans.</b>  Yes. The time defined in the Postal Ballot Rules, 2001 for Postal Ballot is applicable for
        e-Voting as well. <br />
      
        <p class="style1"><b>Q.10  Can a shareholder modify a vote already casted before the closing of e-Voting cycle?</b></p> <br />
        <b>Ans.</b>  No. Vote once casted will be considered final and cannot be modified. <br />
      
      <p class="style1"><b>Q.11  Voting on selective resolutions is permitted in Postal Ballot. Is the same available in 
          the e-Voting System as well ?</b></p> <br />
        <b> Ans.</b>  Yes, since a shareholder can decide to vote only on some of the resolutions put to vote, <br />
        the same can also be done in e-Voting System as well. <br />
      
