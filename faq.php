    <p><b>Q.1  What is e-Voting?</b> <br />
        <br>
  <b>Ans.</b>  e-Voting is  voting  through  an electronic system where  shareholders can vote on <br />
  resolutions of  companies  requiring voting through Postal Ballot  as per extant rules  and <br />
  regulations without having the necessity of sending their votes through post. Ministry of <br />
  Corporate Affairs has authorised NSDL for setting up an electronic platform to facilitate <br />
  shareholders to cast vote in electronic form. Accordingly, NSDL has set-up an electronic <br />
  infrastructure to facilitate shareholders to cast votes in electronic form through internet. <br />
</p>
    <p><b>Q.2  What is the legal framework permitting e-Voting?</b> <br />
        <br>
    <b>Ans.</b>  The Companies (Passing of the Resolution by Postal Ballot) Rules, 2001, has defined the <br />
&ldquo;Postal Ballot as voting by shareholders by postal or electronic mode instead of voting <br />
personally by presenting for transacting businesses in a general meeting of the company&rdquo;. <br />
  Thus, Section 192A of the Companies Act, 1956 read with Companies (Passing of the <br />
  Resolution by Postal Ballot) Rules, 2001 recognizes voting by electronic mode for postal <br />
  ballot.  The Postal Ballot Rules, 2001 were Superceded by  Postal Ballot Rules, 2011, <br />
  which prescribed the appropriate mechanism for e-Voting. <br />
</p>
<p>
    <b>Q.3  How will I benefit from the e-Voting System?</b> <br />
    <br>
    <b>Ans.</b>  (i)  Substantial reduction in administration cost viz., <br />
&#61692;  Postal correspondence <br />
&#61692; Verification of signatures <br />
&#61692; Counting of votes <br />
&#61692; Storing of physical ballot papers <br />
  (ii)  Accuracy in counting of votes. <br />
</p>
